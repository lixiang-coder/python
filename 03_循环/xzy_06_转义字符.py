# `\t` 在控制台输出一个 制表符，协助在输出文本时 垂直方向 保持对齐
# `\n` 在控制台输出一个 换行符
print("1 2 3")
print("10 20 30")
print("-" * 50)

# 使用 \t 转义字符
print("1\t2\t3")
print("10\t20\t30")

print("-" * 50)
# 使用 \n 换行符
print("hello\nworld")

print("-" * 50)
# 使用 \"
print("你好，\"我叫薛朝阳")

print("-" * 50)
# 使用 \\ 换行符
print("你叫\\什么名字？")
